  function getFrequentSymbolSeries(data, minCount = 4) {
    const countMap = {};
    const result = [];
  
    // Count occurrences of each SymbolSeries with its CMP
    data.forEach(item => {
      const symbolSeries = item ? item.SymbolSeries.trim() : '';
      const cmp = item.CMP ? item.CMP.trim() : '';
  
      if (symbolSeries && cmp) {
        const key = `${symbolSeries}${cmp}`;
        if (countMap[key]) {
          countMap[key].count += 1;
        } else {
          countMap[key] = { count: 1, SymbolSeries: symbolSeries, CMP: cmp };
        }
      }
    });
  
    // Filter SymbolSeries that appear more than minCount times
    for (const key in countMap) {
      if (countMap[key].count > minCount) {
        result.push(countMap[key]);
      }
    }
  
    return result;
  }
  
